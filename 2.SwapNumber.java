class Swap {
  public static void main(String[] args) {
    // we have swap the value of the variable
    // a=10
    // b=5
    // output shoud be
    // a=5
    // b=10
    int a = 10;
    int b = 5;
    int c = a;
    a = b;
    b = c;
    System.out.println("a:" + a);
    System.out.println("b:" + b);

  }

}
